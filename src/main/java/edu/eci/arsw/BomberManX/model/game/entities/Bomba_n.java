package edu.eci.arsw.BomberManX.model.game.entities;

/**
 *
 * @author sergioxl
 */
public class Bomba_n implements Elemento{

    private int posRow;
    private int posCol;
    private String key;

    public Bomba_n(String key, int posRow, int posCol) {
        this.posRow = posRow;
        this.posCol = posCol;
        this.key = key;
    }
 
    @Override
    public int getPosRow() {
        return this.posRow;
    }

    @Override
    public void setPosRow(int pos) {
        this.posRow = pos;
    }

    @Override
    public int getPosCol() {
        return this.posCol;
    }

    @Override
    public void setPosCol(int pos) {
        this.posCol = pos;
    }
    
    @Override
    public String getKey() {
        return this.key;
    }

    @Override
    public void setKey(String k) {
        this.key = k;
    }
    
}
